let weather='rain';
switch(weather){
    case 'rain':
        window.alert('its rainy season');
        break;
    case 'sun':
        window.alert("its summer season")
        break;
    case 'wind':
        window.alert("its autumn season");
        break;
    case 'leaf':
        window.alert("its spring season");
        break;
    case 'snow':
        window.alert("its winter season");
        break;
    
}